namespace DocProcessor;

public static class Parse
{

    public static void ParseStatement(string statement)
    {
        for(int i = 0; i < statement.Length; i++)
        {
            char c = statement[i];
            switch (c)
            {
                case '[':
                    break;
                
                case ']':
                    break;
                
                case 'i':
                    break;
                
                case ':':
                    break;
                
                default:
                    break;
                
            }
        }
    }
          
}